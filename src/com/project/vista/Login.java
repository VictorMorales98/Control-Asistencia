    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        Conexion con = new Conexion();
        Admin a = new Admin();
        
        String pas = new String(txtContrasena.getPassword());
        
        if (!txtUsuario.getText().equals("") && !txtContrasena.getPassword().equals("")){
            a.setPas(pas);
            a.setUsu(txtUsuario.getText());
            
            if(con.login(a)){
                Principal p = new Principal(a);
                p.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Datos Incorrectos", "Usuario No Encotrado",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingresar Datos", "Campos Vacios", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jLabel7MouseClicked

